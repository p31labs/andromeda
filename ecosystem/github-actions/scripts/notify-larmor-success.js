#!/usr/bin/env node

/**
 * Notify Larmor Success
 * Sends Discord notifications about successful Larmor frequency synchronization
 */

const fs = require('fs');
const path = require('path');

class LarmorSuccessNotifier {
    constructor() {
        this.discordWebhook = process.env.DISCORD_LARMOR_WEBHOOK;
        this.ensDomain = process.env.ENS_DOMAIN || 'andromeda.p31.eth';
    }

    /**
     * Main notification function
     */
    async notify() {
        try {
            console.log('🎵 Sending Larmor Success Notification...');
            
            // Get input from GitHub Actions
            const userId = process.env.INPUT_USER_ID;
            const resonanceLevel = parseInt(process.env.INPUT_RESONANCE_LEVEL) || 10;
            const decryptedCID = process.env.INPUT_DECRYPTED_CID;
            const publishedCID = process.env.INPUT_PUBLISHED_CID;
            
            // Generate notification
            const notification = this.generateNotification(userId, resonanceLevel, decryptedCID, publishedCID);
            
            // Send notification
            await this.sendNotification(notification);
            
            // Log notification
            this.logNotification(notification);
            
            console.log('✅ Larmor success notification sent successfully');
            return notification;
            
        } catch (error) {
            console.error('❌ Failed to send Larmor success notification:', error);
            process.exit(1);
        }
    }

    /**
     * Generate notification content
     */
    generateNotification(userId, resonanceLevel, decryptedCID, publishedCID) {
        const notification = {
            embed: {
                title: '🎵 Larmor Frequency Lock Unlocked!',
                description: `
**Quantum Resonance Achieved!** 

${userId} has successfully synchronized to the Larmor precession frequency of Phosphorus-31 in Earth's magnetic field (0.86 Hz).

### 🎯 Synchronization Details
- **Resonance Level:** ${resonanceLevel}/10
- **Frequency:** 0.86 Hz (Larmor precession)
- **Target:** Phosphorus-31 quantum states
- **Achievement:** Lore unlock sequence initiated

### 📡 Content Unlocked
**Decrypted IPFS CID:** \`${decryptedCID}\`
**Published CID:** \`${publishedCID}\`

### 🌐 Access Points
- **IPFS Gateway:** https://ipfs.io/ipfs/${publishedCID}
- **IPNS Domain:** ipns://${this.ensDomain}
- **ENS Domain:** https://${this.ensDomain}

### 🏆 Community Impact
- **Karma Awarded:** +${this.calculateKarmaAward(resonanceLevel)}
- **Spoons Expended:** 1 (resonance attempt)
- **Node Status:** Enhanced
- **Achievement Unlocked:** Larmor Frequency Master

### 🔬 Scientific Significance
This synchronization represents a successful quantum biological interface with Phosphorus-31 nuclear spins. The 0.86 Hz frequency corresponds to the Larmor precession of ³¹P in Earth's magnetic field, enabling quantum information transfer and biological coherence enhancement.

---
*This notification was generated by the P31 Quantum Resonance Monitoring System*
                `,
                color: 0x00ffff, // Cyan color for quantum/tech theme
                fields: [
                    {
                        name: 'Resonance Quality',
                        value: this.getResonanceQuality(resonanceLevel),
                        inline: true
                    },
                    {
                        name: 'Frequency Precision',
                        value: 'Quantum Level',
                        inline: true
                    },
                    {
                        name: 'Community Status',
                        value: 'Active',
                        inline: true
                    }
                ],
                footer: {
                    text: 'P31 Quantum Resonance System',
                    icon_url: 'https://phosphorus31.org/favicon.ico'
                },
                timestamp: new Date().toISOString(),
                image: {
                    url: 'https://phosphorus31.org/assets/larmor-resonance.png'
                }
            }
        };
        
        return notification;
    }

    /**
     * Calculate karma award
     */
    calculateKarmaAward(resonanceLevel) {
        const baseKarma = 50;
        const resonanceBonus = resonanceLevel * 5;
        return baseKarma + resonanceBonus;
    }

    /**
     * Get resonance quality description
     */
    getResonanceQuality(resonanceLevel) {
        if (resonanceLevel === 10) return 'Perfect (10/10) - Quantum Coherence Achieved';
        if (resonanceLevel >= 8) return 'Excellent (8-9/10) - High Precision';
        if (resonanceLevel >= 6) return 'Good (6-7/10) - Stable Resonance';
        if (resonanceLevel >= 4) return 'Fair (4-5/10) - Partial Synchronization';
        return 'Basic (1-3/10) - Learning Phase';
    }

    /**
     * Send notification to Discord
     */
    async sendNotification(notification) {
        if (!this.discordWebhook) {
            console.log('⚠️ Discord webhook not configured, skipping notification');
            return;
        }
        
        try {
            const response = await fetch(this.discordWebhook, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    embeds: [notification.embed]
                })
            });
            
            if (!response.ok) {
                throw new Error(`Discord API error: ${response.status}`);
            }
            
            console.log('✅ Discord notification sent successfully');
            
        } catch (error) {
            console.error('❌ Failed to send Discord notification:', error);
            throw error;
        }
    }

    /**
     * Log notification
     */
    logNotification(notification) {
        const logPath = path.join(__dirname, '..', '..', 'larmor-success-notifications.log');
        
        const logEntry = {
            timestamp: new Date().toISOString(),
            type: 'larmor_success',
            title: notification.embed.title,
            description: notification.embed.description,
            fields: notification.embed.fields
        };
        
        const logLine = JSON.stringify(logEntry) + '\n';
        fs.appendFileSync(logPath, logLine);
        
        console.log(`💾 Larmor success notification logged to: ${logPath}`);
    }

    /**
     * Generate achievement notification
     */
    generateAchievementNotification(userId, achievementType, resonanceLevel) {
        const achievement = {
            'first_resonance': {
                title: '🎵 First Resonance Achieved!',
                description: `${userId} has achieved their first Larmor frequency synchronization! Welcome to the quantum resonance community.`,
                color: 0x00ff00
            },
            'precision_master': {
                title: '🎯 Precision Master Unlocked!',
                description: `${userId} has achieved 100% precision in Larmor frequency synchronization. Quantum timing mastery demonstrated!`,
                color: 0xffff00
            },
            'quantum_entangler': {
                title: '🔗 Quantum Entangler Achieved!',
                description: `${userId} has achieved resonance 10 times. Quantum entanglement with the P31 network established!`,
                color: 0xff00ff
            },
            'frequency_legend': {
                title: '🌟 Frequency Legend Unlocked!',
                description: `${userId} has achieved maximum resonance level 5 times. True mastery of quantum frequencies demonstrated!`,
                color: 0xffd700
            }
        };
        
        return {
            embed: {
                title: achievement[achievementType].title,
                description: achievement[achievementType].description,
                color: achievement[achievementType].color,
                fields: [
                    {
                        name: 'Achievement Type',
                        value: achievementType.replace('_', ' ').toUpperCase(),
                        inline: true
                    },
                    {
                        name: 'Resonance Level',
                        value: `${resonanceLevel}/10`,
                        inline: true
                    },
                    {
                        name: 'Frequency',
                        value: '0.86 Hz',
                        inline: true
                    }
                ],
                footer: {
                    text: 'P31 Quantum Resonance System',
                    icon_url: 'https://phosphorus31.org/favicon.ico'
                },
                timestamp: new Date().toISOString()
            }
        };
    }

    /**
     * Generate community-wide notification
     */
    generateCommunityNotification(totalResonances, activeUsers, latestAchievement) {
        return {
            embed: {
                title: '🌐 Community Resonance Update',
                description: `
**P31 Network Status Report**

The community has achieved significant progress in quantum biological synchronization. Keep up the excellent work!

### 📊 Network Statistics
- **Total Resonances:** ${totalResonances}
- **Active Users:** ${activeUsers}
- **Latest Achievement:** ${latestAchievement}

### 🎵 Frequency Status
- **Target Frequency:** 0.86 Hz (Larmor precession)
- **Network Coherence:** Active
- **Quantum Entanglement:** Growing

### 🚀 What's Next?
Continue practicing your synchronization skills and helping other community members achieve resonance. Every successful synchronization strengthens the collective quantum biological network.

---
*Join the resonance at https://phosphorus31.org/bonding*
                `,
                color: 0x0088ff,
                fields: [
                    {
                        name: 'Network Health',
                        value: 'Optimal',
                        inline: true
                    },
                    {
                        name: 'Community Activity',
                        value: 'High',
                        inline: true
                    },
                    {
                        name: 'Quantum Coherence',
                        value: 'Growing',
                        inline: true
                    }
                ],
                footer: {
                    text: 'P31 Quantum Resonance System',
                    icon_url: 'https://phosphorus31.org/favicon.ico'
                },
                timestamp: new Date().toISOString()
            }
        };
    }
}

// Execute if run directly
if (require.main === module) {
    const notifier = new LarmorSuccessNotifier();
    notifier.notify().then(notification => {
        console.log('\n🎵 Larmor success notification sent successfully!');
        console.log(`   Title: ${notification.embed.title}`);
        console.log(`   User: ${notification.embed.description.match(/@(\w+)/)?.[1] || 'Unknown'}`);
    });
}

module.exports = LarmorSuccessNotifier;