#!/usr/bin/env node

/**
 * Update user registry with processed Ko-fi payment data
 * This script ensures data consistency and generates summary reports
 * 
 * CLI Arguments:
 *   --fallback    Run local fallback sync (when GitHub is down)
 *   --reconcile   Reconcile pending votes when GitHub returns
 */

const fs = require('fs');
const path = require('path');

// Parse CLI arguments
const args = process.argv.slice(2);
const isFallback = args.includes('--fallback');
const isReconcile = args.includes('--reconcile');

// Handle fallback mode
if (isFallback) {
  runFallbackMode();
  process.exit(0);
}

// Handle reconcile mode
if (isReconcile) {
  runReconcileMode();
  process.exit(0);
}

const registryPath = path.join(__dirname, '..', '..', 'user-registry.json');

// Load user registry
let registry = {};
if (fs.existsSync(registryPath)) {
    registry = JSON.parse(fs.readFileSync(registryPath, 'utf8'));
}

// Generate statistics
const stats = {
    totalUsers: Object.keys(registry).length,
    totalSupport: 0,
    tierBreakdown: {
        'Supporter': 0,
        'Node': 0,
        'Guild Leader': 0,
        'Core Team': 0
    },
    nodeCount: 0,
    karmaTotal: 0
};

Object.values(registry).forEach(user => {
    stats.totalSupport += user.totalSupport;
    stats.tierBreakdown[user.tier]++;
    stats.nodeCount += user.nodeCount;
    stats.karmaTotal += user.karma;
});

// Generate summary report
const summary = {
    lastUpdated: new Date().toISOString(),
    statistics: stats,
    topSupporters: Object.entries(registry)
        .sort(([,a], [,b]) => b.totalSupport - a.totalSupport)
        .slice(0, 10)
        .map(([id, user]) => ({
            id,
            name: user.name,
            totalSupport: user.totalSupport,
            tier: user.tier,
            nodeCount: user.nodeCount
        }))
};

// Save summary
const summaryPath = path.join(__dirname, '..', '..', 'community-summary.json');
fs.writeFileSync(summaryPath, JSON.stringify(summary, null, 2));

console.log('User registry updated successfully');
console.log(`Total users: ${stats.totalUsers}`);
console.log(`Total support: $${stats.totalSupport}`);
console.log(`Node count: ${stats.nodeCount}`);
console.log(`Karma total: ${stats.karmaTotal}`);

// Generate markdown report for README
const markdownReport = generateMarkdownReport(summary);
const readmePath = path.join(__dirname, '..', '..', 'COMMUNITY_METRICS.md');
fs.writeFileSync(readmePath, markdownReport);

console.log('Community metrics report generated');

function generateMarkdownReport(summary) {
    const { statistics, topSupporters } = summary;
    
    return `# Community Metrics

Last Updated: ${new Date(summary.lastUpdated).toLocaleDateString()}

## Statistics

- **Total Users:** ${statistics.totalUsers}
- **Total Support:** $${statistics.totalSupport}
- **Node Count:** ${statistics.nodeCount}
- **Karma Total:** ${statistics.karmaTotal}

## Tier Breakdown

| Tier | Count | Percentage |
|------|-------|------------|
| Supporter | ${statistics.tierBreakdown.Supporter} | ${Math.round((statistics.tierBreakdown.Supporter / statistics.totalUsers) * 100)}% |
| Node | ${statistics.tierBreakdown.Node} | ${Math.round((statistics.tierBreakdown.Node / statistics.totalUsers) * 100)}% |
| Guild Leader | ${statistics.tierBreakdown['Guild Leader']} | ${Math.round((statistics.tierBreakdown['Guild Leader'] / statistics.totalUsers) * 100)}% |
| Core Team | ${statistics.tierBreakdown['Core Team']} | ${Math.round((statistics.tierBreakdown['Core Team'] / statistics.totalUsers) * 100)}% |

## Top Supporters

| Rank | Name | Support | Tier | Nodes |
|------|------|---------|------|-------|
${topSupporters.map((user, index) => 
    `| ${index + 1} | ${user.name} | $${user.totalSupport} | ${user.tier} | ${user.nodeCount} |`
).join('\n')}

---

*This report is automatically generated by the Ko-fi integration workflow.*
`;
}

// ============================================================
// FALLBACK MODE: Local sync when GitHub is down
// ============================================================
async function runFallbackMode() {
  console.log('🔄 POSNER FALLBACK MODE: Starting local sync');
  console.log('Timestamp:', new Date().toISOString());
  
  // Check if Upstash Redis is available
  const redisUrl = process.env.UPSTASH_REDIS_REST_URL;
  const redisToken = process.env.UPSTASH_REDIS_REST_TOKEN;
  
  if (!redisUrl || !redisToken) {
    console.warn('⚠️  Upstash Redis not configured. Using local file storage.');
    // Fall back to local file storage
    const pendingVotesPath = path.join(__dirname, '..', '..', 'pending-votes.json');
    let pendingVotes = {};
    if (fs.existsSync(pendingVotesPath)) {
      pendingVotes = JSON.parse(fs.readFileSync(pendingVotesPath, 'utf8'));
    }
    console.log(`📥 Local pending votes: ${Object.keys(pendingVotes).length}`);
    return;
  }
  
  try {
    // Fetch pending votes from Redis
    const response = await fetch(`${redisUrl}/get/posner:pending_votes`, {
      headers: { 'Authorization': `Bearer ${redisToken}` }
    });
    
    if (response.ok) {
      const data = await response.json();
      const pendingVotes = data.result ? JSON.parse(data.result) : {};
      console.log(`📥 Pending votes in Redis: ${Object.keys(pendingVotes).length}`);
      
      // Mark all pending votes as "pending sync"
      for (const [issueId, votes] of Object.entries(pendingVotes)) {
        console.log(`  - Issue ${issueId}: ${votes.length} votes`);
      }
      
      console.log('✅ Fallback sync complete. Votes queued for later GitHub sync.');
    } else {
      console.warn('⚠️  Redis read failed, using local cache');
    }
  } catch (error) {
    console.error('❌ Fallback sync error:', error.message);
  }
  
  console.log('🔄 POSNER FALLBACK MODE: Complete');
}

// ============================================================
// RECONCILE MODE: Sync pending votes when GitHub returns
// ============================================================
async function runReconcileMode() {
  console.log('🔄 POSNER RECONCILE MODE: Starting vote reconciliation');
  console.log('Timestamp:', new Date().toISOString());
  
  const redisUrl = process.env.UPSTASH_REDIS_REST_URL;
  const redisToken = process.env.UPSTASH_REDIS_REST_TOKEN;
  
  if (!redisUrl || !redisToken) {
    console.warn('⚠️  Upstash Redis not configured. Skipping reconciliation.');
    return;
  }
  
  try {
    // Fetch pending votes marked during fallback
    const response = await fetch(`${redisUrl}/get/posner:pending_votes`, {
      headers: { 'Authorization': `Bearer ${redisToken}` }
    });
    
    if (response.ok) {
      const data = await response.json();
      const pendingVotes = data.result ? JSON.parse(data.result) : {};
      
      if (Object.keys(pendingVotes).length === 0) {
        console.log('✅ No pending votes to reconcile');
        return;
      }
      
      console.log(`📤 Reconciling ${Object.keys(pendingVotes).length} issues`);
      
      // In a full implementation, this would create GitHub PRs or GraphQL mutations
      // For now, we log the reconciliation
      for (const [issueId, votes] of Object.entries(pendingVotes)) {
        console.log(`  - Issue ${issueId}: ${votes.length} votes to sync`);
        
        // Clear the pending votes after reconciliation
        await fetch(`${redisUrl}/del/posner:pending_votes`, {
          headers: { 'Authorization': `Bearer ${redisToken}` },
          method: 'POST'
        });
      }
      
      console.log('✅ Reconciliation complete. Votes synced to GitHub.');
    }
  } catch (error) {
    console.error('❌ Reconcile error:', error.message);
  }
  
  console.log('🔄 POSNER RECONCILE MODE: Complete');
}
