// ── Fetch genesis-gate events ─────────────────────────────────────────────

async function fetchServerEvents(baseUrl, adminToken) {
  const since = new Date(Date.now() - 90 * 24 * 60 * 60 * 1000).toISOString();
  const url = `${baseUrl}/events?since=${encodeURIComponent(since)}`;
  const resp = await fetch(url, {
    headers: { Authorization: `Bearer ${adminToken}` },
  });
  if (!resp.ok) throw new Error(`genesis-gate: ${resp.status} ${await resp.text()}`);
  const data = await resp.json();
  return data.events ?? [];
}

// ── Main ──────────────────────────────────────────────────────────────────

async function main() {
  const args = parseArgs();
  const exportedAt = new Date().toISOString();

  let sessions = [];
  let rawServerEvents = null;

  // Load client-side chain data
  if (args.input) {
    const raw = JSON.parse(readFileSync(args.input, 'utf8'));
    // Accept either array of session backstops or a single backstop
    sessions = Array.isArray(raw) ? raw : [raw];
  }

  // Fetch server-side events
  if (args['genesis-gate'] && args['admin-token']) {
    console.error(`[daubert] Fetching server events from ${args['genesis-gate']}…`);
    rawServerEvents = await fetchServerEvents(args['genesis-gate'], args['admin-token']);
    console.error(`[daubert] Fetched ${rawServerEvents.length} server events`);
  }

  // Verify client-side chains
  const verifiedSessions = sessions.map(s => ({
    ...s,
    verification: verifyChain(s.events ?? []),
  }));

  // Verify server-side events
  const serverVerification = rawServerEvents ? verifyServerEvents(rawServerEvents) : null;

  // Build manifest
  const manifest = buildManifest({
    sessions: verifiedSessions,
    serverEvents: serverVerification,
    exportedAt,
  });

  // Output JSON
  const datestamp = exportedAt.slice(0, 10);
  const jsonPath = `daubert-evidence-${datestamp}.json`;
  const htmlPath = `daubert-evidence-${datestamp}.html`;

  writeFileSync(jsonPath, JSON.stringify(manifest, null, 2));
  writeFileSync(htmlPath, renderHTML(manifest));

  // Summary to stderr
  const v = manifest.manifest.integrity;
  console.error(`\n[daubert] Export complete`);
  console.error(`  Sessions:     ${v.totalSessions}`);
  console.error(`  Client events: ${v.totalClientEvents}`);
  console.error(`  Server events: ${v.totalServerEvents}`);
  console.error(`  Chain valid:   ${v.clientChainValid ? 'YES ✓' : 'NO ✗ — INVESTIGATE'}`);
  console.error(`  JSON:  ${jsonPath}`);
  console.error(`  HTML:  ${htmlPath}`);

  if (!v.clientChainValid) {
    console.error('\n[daubert] WARNING: Chain integrity failure detected.');
    console.error('  Check "sessions[*].verification.results" for events with valid=false.');
    exit(1);
  }
}

main().catch(err => { console.error(err); exit(1); });
